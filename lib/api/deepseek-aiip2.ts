interface GeneratePromptParams {
  species: string
  color?: string
  bodyType?: string
  action: string
  location?: string
  festival?: string
  brandInfo?: string
}

/**
 * 生成 AIIP 插画 2 号员工提示词
 */
export async function generateAIIP2Prompt(params: GeneratePromptParams): Promise<string> {
  if (!DEEPSEEK_API_KEY) {
    throw new Error('DEEPSEEK_API_KEY is not configured')
  }

  // 构建用户输入
  const userInput = `
请根据以下信息生成专业的IP角色节日场景插画提示词：

IP角色信息：
- 物种：${params.species}
${params.color ? `- 颜色：${params.color}` : ''}
${params.bodyType ? `- 体型：${params.bodyType}` : ''}

场景与节日：
- 动作：${params.action}
${params.location ? `- 地点：${params.location}` : ''}
${params.festival ? `- 节日：${params.festival}` : ''}

${params.brandInfo ? `品牌信息、理念：\n- ${params.brandInfo}` : ''}

请生成一个完整的、富有逻辑的中文提示词，确保包含所有必需元素和设计巧思。
  `.trim()

  console.log('[DeepSeek AIIP2] 开始生成提示词，用户输入:', userInput)

  try {
    const response = await fetch(`${DEEPSEEK_BASE_URL}/v1/chat/completions`, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${DEEPSEEK_API_KEY}`,
      },
      body: JSON.stringify({
        model: 'deepseek-chat',
        messages: [
          {
            role: 'system',
            content: AIIP2_ILLUSTRATION_SYSTEM_PROMPT
          },
          {
            role: 'user',
            content: userInput
          }
        ],
        temperature: 0.7,
        max_tokens: 1500,
      }),
    })

    if (!response.ok) {
      const errorData = await response.json()
      console.error('[DeepSeek AIIP2] API 调用失败:', errorData)
      throw new Error(`DeepSeek API error: ${response.status} ${response.statusText}`)
    }

    const data = await response.json()
    const generatedPrompt = data.choices?.[0]?.message?.content

    if (!generatedPrompt) {
      throw new Error('DeepSeek API 未返回有效内容')
    }

    console.log('[DeepSeek AIIP2] 提示词生成成功，长度:', generatedPrompt.length)

    return generatedPrompt
  } catch (error) {
    console.error('[DeepSeek AIIP2] 生成提示词失败:', error)
    throw error
  }
}

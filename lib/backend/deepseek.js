/**
 * 调用 DeepSeek 生成品牌海报设计提示词
 */
async function generateBrandPosterPrompts(params) {
  const { productName, sellingPoint, style, aspectRatio } = params;

  let userMessage = `产品：${productName}\n卖点：${sellingPoint}`;
  if (style) userMessage += `\n风格要求：${style}`;
  if (aspectRatio) userMessage += `\n画面比例：${aspectRatio}`;

  try {
    const response = await axios.post(
      `${config.deepseek.baseUrl}/v1/chat/completions`,
      {
        model: 'deepseek-chat',
        messages: [
          { role: 'system', content: BRAND_POSTER_SYSTEM_PROMPT },
          { role: 'user', content: userMessage }
        ],
        temperature: 0.8,
        max_tokens: 4000
      },
      {
        headers: {
          'Authorization': `Bearer ${config.deepseek.apiKey}`,
          'Content-Type': 'application/json'
        }
      }
    );

    const content = response.data.choices[0].message.content;
    const prompts = parseBrandPosterPrompts(content);

    return { success: true, rawContent: content, prompts };
  } catch (error) {
    console.error('DeepSeek API 调用失败:', error.response?.data || error.message);
    throw new Error('生成品牌海报提示词失败: ' + (error.response?.data?.error?.message || error.message));
  }
}

